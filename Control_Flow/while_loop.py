"""
Topic: while loop in Python

Used when number of iterations is unknown.
"""

# --------------------------------------------------
# REAL-TIME EXAMPLE: LOGIN ATTEMPTS
# --------------------------------------------------

attempts = 1

while attempts <= 3:
    print("Attempt:", attempts)
    attempts += 1

# Output:
# Attempt: 1
# Attempt: 2
# Attempt: 3


# --------------------------------------------------
# RULES
# --------------------------------------------------
# 1. Condition must become False
# 2. Infinite loop occurs if not updated
# 3. Best for condition-based repetition

