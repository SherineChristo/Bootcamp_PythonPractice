"""
Topic: for loop in Python

Used to iterate over sequences.
"""

# --------------------------------------------------
# REAL-TIME EXAMPLE: PRINT SUBJECTS
# --------------------------------------------------

subjects = ["Math", "Physics", "AI"]

for subject in subjects:
    print(subject)

# Output:
# Math
# Physics
# AI


# --------------------------------------------------
# FOR LOOP WITH RANGE
# --------------------------------------------------

for roll_no in range(1, 6):
    print(roll_no)

# Output:
# 1
# 2
# 3
# 4
# 5


# --------------------------------------------------
# RULES
# --------------------------------------------------
# 1. Works with list, tuple, set, string
# 2. range(start, stop) â†’ stop is excluded
# 3. Indentation is mandatory

