"""
Topic: Built-in Functions for Collections
"""

numbers = [10, 20, 30, 40]

print(len(numbers))     # Output: 4
print(max(numbers))     # Output: 40
print(min(numbers))     # Output: 10
print(sum(numbers))     # Output: 100
print(sorted(numbers)) # Output: [10, 20, 30, 40]

# --------------------------------------------------
# RULES
# --------------------------------------------------
# 1. sorted() returns a new list
# 2. sum() works only for numeric values
# 3. len() works on all collections

